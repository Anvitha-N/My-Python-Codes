#Assigning elements to different lists:
animals = ["dog","monkey","elephant","giraffee"]
print(animals)

#replace
animals[1] = "chrocodile"
print(animals)

#insert
animals.insert(2,"squirrel")
print(animals)

#sort
animals.sort()
print(animals)

#delete
del animals[0]
print(animals)

#append
animals.append("panda")
print(animals)

#reverse
animals.reverse()
print(animals)
